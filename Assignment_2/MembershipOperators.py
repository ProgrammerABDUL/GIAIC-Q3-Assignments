def Membership_Operators():
    """This function demonstrates the examples of membership operators in Python."""

    # Heading
    print("\nMembership Operators: -\n")

    # In
    print("In:")
    result = 1 in [1, 2, 3]
    print(f"Example: 1 in [1, 2, 3] = {result}\n")

    # Not In
    print("Not in:")
    result = 4 not in [1, 2, 3]
    print(f"Example: 4 not in [1, 2, 3] = {result}\n")
def Comparison_Operators(x, y):
    """This function demonstrates the examples of comparison operators in Python."""

    # Heading
    print("\nComparison Operators: -\n")

    # Equal to
    print("Equal to:")
    result = x == y
    print(f"Example: {x} == {y} = {result}\n")

    # Not equal to
    print("Not equal to:")
    result = x != y
    print(f"Example: {x} != {y} = {result}\n")

    # Greater than
    print("Greater than:")
    result = x > y
    print(f"Example: {x} > {y} = {result}\n")

    # Less than
    print("Less than:")
    result = x < y
    print(f"Example: {x} < {y} = {result}\n")

    # Greater than or equal to
    print("Greater than or equal to:")
    result = x >= y
    print(f"Example: {x} >= {y} = {result}\n")

    # Less than or equal to
    print("Less than or equal to:")
    result = x <= y
    print(f"Example: {x} <= {y} = {result}\n")
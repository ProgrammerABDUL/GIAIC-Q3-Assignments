def Airthmetic_Operators(x, y):
    """This function demonstrates the examples of airthmetic operators in Python."""

    # Heading
    print("\nAirthmetic Operators: -\n")

    # Addition
    print("Addition:")
    result = x + y
    print(f"Example: {x} + {y} = {result}\n")

    # Subtraction
    print("Subtraction:")
    result = x - y
    print(f"Example: {x} - {y} = {result}\n")

    # Multiplication
    print("Multiplication:")
    result = x * y
    print(f"Example: {x} * {y} = {result}\n")

    # Division
    print("Division:")
    result = x / y
    print(f"Example: {x} / {y} = {result}\n")

    # Floor Division
    print("Floor Division:")
    result = x // y
    print(f"Example: {x} // {y} = {result}\n")

    # Modulus
    print("Modulus:")
    result = x % y
    print(f"Example: {x} % {y} = {result}\n")

    # Exponentiation
    print("Exponentiation:")
    result = x ** y
    print(f"Example: {x} ** {y} = {result}\n")